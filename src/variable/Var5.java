package variable;

public class Var5 {
    public static void main(String[] args) {
        // 1. 변수 선언과 초기화를 각각 따로 진행
        int a;
        a = 10;
        System.out.println(a);

        // 2. 변수를 선언과 초기화를 한번에
        int b = 10;
        System.out.println(b);

        // 3. 여러 변수를 선언과 초기화를 한번에
        int c = 20, d = 30;
        System.out.println(c);
        System.out.println(d);
    }
}
